# 문제 설명
# 하노이 탑(Tower of Hanoi)은 퍼즐의 일종입니다. 세 개의 기둥과 이 기동에 꽂을 수 있는 크기가 다양한 원판들이 있고, 퍼즐을 시작하기 전에는 한 기둥에 원판들이 작은 것이 위에 있도록 순서대로 쌓여 있습니다. 게임의 목적은 다음 두 가지 조건을 만족시키면서, 한 기둥에 꽂힌 원판들을 그 순서 그대로 다른 기둥으로 옮겨서 다시 쌓는 것입니다.
#
# 한 번에 하나의 원판만 옮길 수 있습니다.
# 큰 원판이 작은 원판 위에 있어서는 안됩니다.
# 하노이 탑의 세 개의 기둥을 왼쪽 부터 1번, 2번, 3번이라고 하겠습니다. 1번에는 n개의 원판이 있고 이 n개의 원판을 3번 원판으로 최소 횟수로 옮기려고 합니다.
#
# 1번 기둥에 있는 원판의 개수 n이 매개변수로 주어질 때, n개의 원판을 3번 원판으로 최소로 옮기는 방법을 return하는 solution를 완성해주세요.
#
# 제한사항
# n은 15이하의 자연수 입니다.


#내풀이
#각 기둥 -> 스택 구조
#원판 크기 구별 -> 정수의 크기로
# 탐색 방식으로 풀어야 될 거 같음.
# 규칙
# 이미 한 번 겪었던 상태로 돌아가면 안됨. -> 방문기록을 통해서 방지
# 쌓을 때는 기둥에서 가장 꼭대기에 있는 원판들만 옮길 수 있음. (경우의 수 A)
# 옮길 때는 기둥 끝에 있는 원판이 옮기는 원판의 숫자보다 크거나 기둥에 원판이 없어야 함.  (경우의 수 B)
# A * B 해서 큐에 넣어서 탐색하면 되지 않을까? 큐에 넣을 때 원소 형태는 {몇번째 이동하는지 : [옮길 기둥, 옮겨진 기둥] & 옮기기 전 기둥 현황}
# 옮긴 후 이미 겪어본 기둥 상황이라면 다시 탐색X, 새로운 상황이라면 삽입

def solution(n):
    answer = []

    # helper 함수: n개의 원판을 start에서 end로 옮김 (mid는 거쳐가는 기둥)
    def moved_pan(n, start, end, mid):
        # 원판이 1개일 때는 그냥 목적지로 옮기면 끝! (Base Case)
        if n == 1:
            answer.append([start, end])
            return

        # 1. n-1개를 시작 기둥에서 중간 기둥으로 옮깁니다.
        moved_pan(n - 1, start, mid, end)

        # 2. 가장 큰 원판(n번째)을 목적 기둥으로 옮깁니다.
        answer.append([start, end])

        # 3. 중간에 치워뒀던 n-1개를 다시 목적 기둥으로 옮깁니다.
        moved_pan(n - 1, mid, end, start)

    # 1번 기둥에 있는 n개를 3번으로 옮기기 시작 (2번은 거쳐가는 용도)
    moved_pan(n, 1, 3, 2)

    return answer